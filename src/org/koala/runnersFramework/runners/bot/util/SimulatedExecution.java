package org.koala.runnersFramework.runners.bot.util;

public class SimulatedExecution {
	double makespanMinutes;
	long cost;
	int noMachinesOverM;
	
	
	public SimulatedExecution(double makespanMinutes, long cost,
			int noMachinesOverM) {
		super();
		this.makespanMinutes = makespanMinutes;
		this.cost = cost;
		this.noMachinesOverM = noMachinesOverM;
	}
	
	
}
